		}

		public Parent GetParent(int ParentId)
		{
            var rc = from p in _parents
                     where p.ParentId == ParentId
                     select p;
            return rc.FirstOrDefault();
		}

		public Child GetChild(int ChildId)
		{
            var rc = from c in _children
                     where c.ChildId == ChildId
                     select c;
            return rc.FirstOrDefault();
		}

		public IEnumerable<Child> GetChildrenByParentId(int ParentId)
		{
            var rc = from c in _children
                     where c.ParentId == ParentId
                     orderby c.Name
                     select c;
            return rc;
        }

		public Toy GetToy(int ToyId)
		{
            var rc = from t in _toys
                     where t.ToyId == ToyId
                     orderby t.Name
                     select t;
